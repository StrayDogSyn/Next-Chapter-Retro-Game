/**
 * World data — a Metroidvania-style interconnected map of 24 single-screen
 * rooms (ADR-004). Rooms are 40x22 tiles of 16px (640x352). Exits are edge
 * openings; the graph below says which room each edge leads to. The level
 * loader (levelLoader.ts) validates every room's dimensions and that every
 * declared exit actually has an opening, so a typo here fails loudly at load
 * rather than silently producing a sealed room.
 *
 * Legend:
 *   #  solid rock (auto-tiled at render)   .  empty
 *   -  one-way platform                    ^  spikes (hazard)
 *   P  player spawn (start room only)      C  treasure chest (rolls loot)
 *   c  coin                                +  health pickup
 *   b  bat   g  goblin   i  imp   f  demon flower turret
 *   Y  wyrmwolf mini-boss   M  war mech mini-boss   W  werewolf final boss
 *   K  ancient key pickup
 *   D  locked door (opens with ancient key)
 *   d  beast door (opens when the wyrmwolf is slain)
 *   J  double-jump upgrade (Aether Wings)   A  dash module
 *
 * Zones control background art + music, reusing the confirmed asset set:
 *   outskirts -> parallax mountain layers      caverns -> mangrove painting
 *   hive      -> living-tissue tile            sky     -> cloud painting
 *   citadel   -> mountain layers, night shift
 */

